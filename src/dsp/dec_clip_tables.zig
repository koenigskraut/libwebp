var sclip2_runtime = [_]u8{0} ** (112 + 112 + 1);
var clip1_runtime = [_]u8{0} ** (255 + 511 + 1);

/// clips [-1020, 1020] to [-128, 127]
pub const VP8ksclip1: [*]const i8 = @ptrCast((if (build_options.use_static_tables) sclip1 else sclip1_runtime)[1020..]);
/// clips [-112, 112] to [-16, 15]
pub const VP8ksclip2: [*]const i8 = @ptrCast((if (build_options.use_static_tables) sclip2 else sclip2_runtime)[112..]);
/// clips [-255,511] to [0,255]
pub const VP8kclip1: [*]const u8 = (if (build_options.use_static_tables) clip1 else clip1_runtime)[255..];
/// abs(x) for x in [-255,255]
pub const VP8kabs0: [*]const u8 = (if (build_options.use_static_tables) abs0 else abs0_runtime)[255..];

comptime {
    @export(VP8ksclip1, .{ .name = "VP8ksclip1" });
    @export(VP8ksclip2, .{ .name = "VP8ksclip2" });
    @export(VP8kclip1, .{ .name = "VP8kclip1" });
    @export(VP8kabs0, .{ .name = "VP8kabs0" });
}

pub const VP8InitClipTables = webp.WEBP_DSP_INIT_FUNC(struct {
    fn _() void {
        if (comptime build_options.use_static_tables) return;
        var i: isize = -255;
        while (i <= 255) : (i += 1) {
            abs0_runtime[@intCast(255 + i)] = @truncate(@abs(i));
        }
        i = -1020;
        while (i <= 1020) : (i += 1) {
            sclip1_runtime[@intCast(1020 + i)] = if (i < -128) @bitCast(@as(i8, -128)) else if (i > 127) 127 else @bitCast(@as(i8, @truncate(i)));
        }
        i = -112;
        while (i <= 112) : (i += 1) {
            sclip2_runtime[@intCast(112 + i)] = if (i < -16) @bitCast(@as(i8, -16)) else if (i > 15) 15 else @bitCast(@as(i8, @truncate(i)));
        }
        i = -255;
        while (i <= 255 + 255) : (i += 1) {
            clip1_runtime[@intCast(255 + i)] = if (i < 0) 0 else if (i > 255) 255 else @bitCast(@as(i8, @truncate(i)));
        }
    }
}._);

pub fn VP8InitClipTables_C() callconv(.C) void {
    VP8InitClipTables();
}
comptime {
    @export(VP8InitClipTables_C, .{ .name = "VP8InitClipTables" });
}
